    public void PopularJTable() {
        Connection con = new ConnectionFactory().getConnection();
        String itemText = (String)jComboBox1.getSelectedItem( );
        String tipoconta = "-1";
        if(itemText=="Energia"){
            tipoconta="e";
        }else if(itemText=="Água"){
            tipoconta="a";
        }else if(itemText=="Ambos"){
            tipoconta="2";
        }
        if(tipoconta=="-1"){
            JOptionPane.showMessageDialog(null, "Favor selecionar um Tipo de Conta");
        }else{
            String sql;
            if(tipoconta=="2"){
                sql = "select distinct mescon,anocon,valcon,tipcon from cliente natural join conta where nomcli='"+jTextField1.getText()+"' order by mescon,anocon";
            }else{
                sql = "select distinct mescon,anocon,valcon,tipcon from cliente natural join conta where tipcon='"+tipoconta+"' and nomcli='"+jTextField1.getText()+"' order by mescon,anocon";
            }
            try
            {
             PreparedStatement banco = con.prepareStatement(sql);
              banco.execute(); // cria o vetor

             ResultSet resultado = banco.executeQuery(sql);

             DefaultTableModel model =(DefaultTableModel) jTable2.getModel();
             model.setNumRows(0);

             while(resultado.next())
             {
                 model.addRow(new Object[] 
                 { 
                    //retorna os dados da tabela do BD, cada campo e um coluna.
                    resultado.getInt("mescon"),
                    resultado.getInt("anocon"),
                    resultado.getString("tipcon"),
                    resultado.getInt("valcon")
                    
                 }); 
            } 
             banco.close();
             con.close();
            }
           catch (SQLException ex)
           {
              System.out.println("o erro foi " +ex);
            }
        }
    }
